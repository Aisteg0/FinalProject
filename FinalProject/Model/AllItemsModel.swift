struct ItemsModel: Codable {
    let success: Bool
    let data: DataClass
}

struct DataClass: Codable {
    let items: [DataItem]
}

struct DataItem: Codable {
    let id: String
    let internalID, licenseID: Int
    let messengerType, type: String
    let phone: String?
    let username, email: JSONNull?
    let name: String
    let image: String
    let lastTime, unreadMessages, lastRead, timeLastInMessage: Int
    let conversation: Conversation?
    let banTime: Int?
    let link: String?
    let tags: [Tag]?
    let responsible: Responsible
    let producer: Responsible?
    let accomplices: [Responsible]?
    let auditors: [Responsible]
    let status: String?
    let level: Responsible?
    let responsiblePin: Bool
    let process: String?
    let search: Search?
    let advert: Advert?
    let secondLink: String?
    let subtype: JSONNull?
    let inviteLink: String?
    let lastMessage: LastMessage?

    enum CodingKeys: String, CodingKey {
        case id
        case internalID = "internalId"
        case licenseID = "licenseId"
        case messengerType, type, phone, username, email, name, image, lastTime, unreadMessages, lastRead, timeLastInMessage, conversation, banTime, link, tags, responsible, producer, accomplices, auditors, status, level, responsiblePin, process, search, advert, secondLink, subtype, inviteLink, lastMessage
    }
}

struct Responsible: Codable {
    let id: Int
}

struct Advert: Codable {
